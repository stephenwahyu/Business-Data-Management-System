export function getPekanbaruPolygonForLeaflet() {
    return PEKANBARU_BOUNDARY.map(coord => [coord[0], coord[1]]);
}

/**
 * Check if coordinates are valid Pekanbaru coordinates with detailed error
 * @param {number} lat - Latitude
 * @param {number} lng - Longitude
 * @returns {Object} - Validation result with error details
 */
export function validatePekanbaruLocation(lat, lng) {
    const numLat = parseFloat(lat);
    const numLng = parseFloat(lng);

    // Check if coordinates are valid numbers
    if (isNaN(numLat) || isNaN(numLng)) {
        return {
            valid: false,
            error: "Invalid coordinates format",
            details: "Latitude and longitude must be valid numbers"
        };
    }

    // Check if coordinates are within Indonesia's general bounds
    // These bounds are very broad and might need refinement for more specific checks
    if (numLat < -11 || numLat > 6 || numLng < 95 || numLng > 141) {
        return {
            valid: false,
            error: "Coordinates outside Indonesia",
            details: "The specified coordinates are not within Indonesia's boundaries"
        };
    }

    // Check if coordinates are within Pekanbaru
    if (!isInsidePekanbaru(numLat, numLng)) {
        const distance = distanceFromPekanbaruBoundary(numLat, numLng);
        return {
            valid: false,
            error: "Location outside Pekanbaru city",
            details: `The specified location is approximately ${distance.toFixed(1)} km outside Pekanbaru city boundaries. Only locations within Pekanbaru city are allowed.`
        };
    }

    return {
        valid: true,
        error: null,
        details: "Location is within Pekanbaru city boundaries"
    };
}
